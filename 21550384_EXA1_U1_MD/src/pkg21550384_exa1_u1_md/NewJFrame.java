    private void btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn1ActionPerformed
        // TODO add your handling code here:
        String sColor1 = txtC1.getText();
        if(sColor1.equalsIgnoreCase("rojo")) {
           txtP1.setBackground(Color.red);
        }
        else if(sColor1.equalsIgnoreCase("azul")) {
           txtP1.setBackground(Color.blue);
        }
        else if(sColor1.equalsIgnoreCase("amarillo")) {
           txtP1.setBackground(Color.yellow);
        }
        else if(sColor1.equalsIgnoreCase("verde")) {
           txtP1.setBackground(Color.green);
        }
        else if(sColor1.equalsIgnoreCase("cian")) {
           txtP1.setBackground(Color.cyan);
        }
        else if(sColor1.equalsIgnoreCase("magenta")) {
           txtP1.setBackground(Color.magenta);
        }
        else{
            JOptionPane.showMessageDialog(null, "No puede usar ese color ", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn1ActionPerformed

